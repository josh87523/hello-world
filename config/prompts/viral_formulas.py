ANTI_AI_PHRASES = """\
## 去AI感检查清单

### 必须删除的AI味词汇：
- 连接词：首先、其次、再次、最后、总之、综上所述、值得注意的是
- 套话：在当今社会、随着...的发展、不可否认、毋庸置疑
- 过度礼貌：希望对你有所帮助、让我们来看看、接下来我将
- 排比结构：连续3个以上"...，...，..."的并列

### 替换为人话：
- "首先" → 直接说
- "综上所述" → "说白了就是"
- "值得注意的是" → "有个坑要注意"
- "不可否认" → 删掉，直接说观点
- "随着AI的发展" → "最近AI太猛了"

### 人设感注入：
- 加口语化表达："说实话""绝了""救命""真的会谢"
- 加个人态度："我觉得""亲测""踩过坑"
- 加不完美感：偶尔用"哈哈""emmm""好吧"
"""

